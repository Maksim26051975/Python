a = []
n = 0
for i in range(int(input("Введите количество элементов в списке: "))):
    a.append((input("Введите очередной элемент списка: ")))
    n += 1
print(f"Исходный   список: {a}")
i = 1
while i < (n):
    if i % 2 != 0:
        x = a[i - 1]
        a[i - 1] = a[i]
        a[i] = x
    i += 1
print(f"Измененный список: {a}")
